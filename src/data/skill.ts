export const NULL_SKILL: UniqueSkill = Object.freeze({
  skillID: -1,
  sortID: -1,
  point: 0,
  type: BUFF_SPEED,
  advanced: false,
  unique: true,
  characterID: -1,
  monikerID: -1,
  matchTalentLevel: FROM_1_TO_5,
  inheritable: false,
});

export const AllSkill: Readonly<Array<Skill | UniqueSkill>> = Object.freeze(
  Skills.sort((x, y) => x.sortID - y.sortID)
);

const _isUniqueSkill = _.memoize((skill: Skill): boolean => {
  return (
    skill.unique &&
    _.has(skill, "characterID") &&
    _.has(skill, "monikerID") &&
    _.has(skill, "matchTalentLevel") &&
    _.has(skill, "inheritable")
  );
});

export function isUniqueSkill(skill: Skill): skill is UniqueSkill {
  return _isUniqueSkill(skill);
}

const skillMap = Object.freeze(
  createNumMap(AllSkill.map((x) => [x.skillID, x]))
);

const uniqueSkills = Object.freeze(AllSkill.filter(isUniqueSkill));

export function getSkill(skillID: number): Skill;
export function getSkill(identify: SkillIdentify): Skill;
export function getSkill(arg: number | SkillIdentify): Skill {
  if (_.isNumber(arg)) {
    return skillMap[arg] || NULL_SKILL;
  }
  return skillMap[arg.skillID] || NULL_SKILL;
}

type UniqueSkillOwner = MonikerIdentify & {
  talentLevel: number;
};

export function isUniqueSkillOwner(
  owner: UniqueSkillOwner,
  uniqueSkill: UniqueSkill
): boolean {
  return (
    uniqueSkill.characterID == owner.characterID &&
    uniqueSkill.monikerID == owner.monikerID &&
    uniqueSkill.matchTalentLevel(owner.talentLevel)
  );
}

export const getUniqueSkill = (owner: UniqueSkillOwner): UniqueSkill => {
  return uniqueSkills.find((x) => isUniqueSkillOwner(owner, x)) || NULL_SKILL;
};
