type Skill = {
  readonly SKILL_ID: number;
  readonly SORT_ID: number;
  // readonly POINT: number;
  readonly TYPE: SkillType;
  readonly CONFLICT_ID?: number;
  readonly ADVANCED: boolean;
  readonly UNIQUE: boolean;
};

type UniqueSkill = {
  readonly SKILL_ID: number;
  readonly SORT_ID: number;
  // readonly POINT: number;
  readonly TYPE: SkillType;
  readonly ADVANCED: false;
  readonly UNIQUE: true;
  readonly CHARACTER_ID: number;
  readonly MONIKER_ID: number;
  MATCH_TALENT_LEVEL(id: number): boolean;
  readonly INHERITABLE: boolean;
};

export const NULL_SKILL: UniqueSkill = {
  SKILL_ID: -1,
  SORT_ID: -1,
  TYPE: BUFF_SPEED,
  ADVANCED: false,
  UNIQUE: true,
  CHARACTER_ID: -1,
  MONIKER_ID: 0,
  MATCH_TALENT_LEVEL: FROM_1_TO_5,
  INHERITABLE: false,
};

export function isUniqueSkill(skill: Skill): skill is UniqueSkill {
  return skill.UNIQUE;
}

const skillMap = Object.freeze(
  createNumMap(Skills.map((x) => [x.SKILL_ID, x]))
);

const uniqueSkills = Object.freeze(
  Skills.filter(isUniqueSkill).map((x) => x as UniqueSkill)
);

export function getSkill(skillID: number): Skill {
  return skillMap[skillID] || NULL_SKILL;
}

type UniqueSkillOwner = {
  characterID: number;
  monikerID: number;
  talentLevel: number;
};

export function isUniqueSkillOwner(
  owner: UniqueSkillOwner,
  uniqueSkill: UniqueSkill
): boolean {
  return (
    uniqueSkill.CHARACTER_ID == owner.characterID &&
    uniqueSkill.MONIKER_ID == owner.monikerID &&
    uniqueSkill.MATCH_TALENT_LEVEL(owner.talentLevel)
  );
}

export function getUniqueSkill(owner: UniqueSkillOwner): UniqueSkill {
  return uniqueSkills.find((x) => isUniqueSkillOwner(owner, x)) || NULL_SKILL;
}
